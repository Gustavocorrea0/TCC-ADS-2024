package Subdivisao;

public class Subdivisao {
    private int idSubdivisao;
    private String nomeSubdivisao;
    private Double areaSubdivisao;
    private String culturaSubdivisao;
    private String nomePropriedade;
    private int idPropriedade;
    
    public String getNomePropriedade() {
        return nomePropriedade;
    }

    public void setNomePropriedade(String nomePropriedade) {
        this.nomePropriedade = nomePropriedade;
    }
    
    public int getIdPropriedade() {
        return idPropriedade;
    }

    public void setIdPropriedade(int idPropriedade) {
        this.idPropriedade = idPropriedade;
    }

    public int getIdSubdivisao() {
        return idSubdivisao;
    }

    public void setIdSubdivisao(int idSubdivisao) {
        this.idSubdivisao = idSubdivisao;
    }

    public String getNomeSubdivisao() {
        return nomeSubdivisao;
    }

    public void setNomeSubdivisao(String nomeSubdivisao) {
        this.nomeSubdivisao = nomeSubdivisao;
    }

    public Double getAreaSubdivisao() {
        return areaSubdivisao;
    }

    public void setAreaSubdivisao(Double areaSubdivisao) {
        this.areaSubdivisao = areaSubdivisao;
    }

    public String getCulturaSubdivisao() {
        return culturaSubdivisao;
    }

    public void setCulturaSubdivisao(String culturaSubdivisao) {
        this.culturaSubdivisao = culturaSubdivisao;
    }
    
    
}
