package praga;

import java.util.Date;

public class Praga {
    
    private int idPraga;
    private String nomePraga;
    private Date dataSurgimentoPraga;
    private String propriedadePraga;
    private String nivelDeAtaquePraga;
    private String estadoDeControlePraga;

    public int getIdPraga() {
        return idPraga;
    }

    public void setIdPraga(int idPraga) {
        this.idPraga = idPraga;
    }

    public String getNomePraga() {
        return nomePraga;
    }

    public void setNomePraga(String nomePraga) {
        this.nomePraga = nomePraga;
    }

    public Date getDataSurgimentoPraga() {
        return dataSurgimentoPraga;
    }

    public void setDataSurgimentoPraga(Date dataSurgimentoPraga) {
        this.dataSurgimentoPraga = dataSurgimentoPraga;
    }

    public String getPropriedadePraga() {
        return propriedadePraga;
    }

    public void setPropriedadePraga(String propriedadePraga) {
        this.propriedadePraga = propriedadePraga;
    }

    public String getNivelDeAtaquePraga() {
        return nivelDeAtaquePraga;
    }

    public void setNivelDeAtaquePraga(String nivelDeAtaquePraga) {
        this.nivelDeAtaquePraga = nivelDeAtaquePraga;
    }

    public String getEstadoDeControlePraga() {
        return estadoDeControlePraga;
    }

    public void setEstadoDeControlePraga(String estadoDeControlePraga) {
        this.estadoDeControlePraga = estadoDeControlePraga;
    }
    
     
}
